// แยกชื่อไทยออกจากป้าย ("BDY-05 กระจังหน้า" → "กระจังหน้า") ทำครั้งเดียว
const CATEGORY_TERMS = STOCK_CATEGORIES.map((c) => {
  const m = /^[A-Z]+-?\d*\s+(.+)$/.exec(c)
  return { label: c, term: (m?.[1] ?? c).trim() }
}).sort((a, b) => b.term.length - a.term.length) // ยาวสุดก่อน = เจาะจงสุด

/**
 * เดาหมวดหมู่จากชื่ออะไหล่ — คืน label เต็ม หรือ null ถ้าไม่มั่นใจ
 * เช่น "กระจังหน้า W140" → "BDY-05 กระจังหน้า"
 */
export function guessCategory(partName: string | null): string | null {
  if (!partName) return null
  const name = partName.trim()
  for (const { label, term } of CATEGORY_TERMS) {
    if (name.includes(term)) return label
  }
  return null
}
